        private void InitData(Year year)
        {
            TBusinessConditions currBusinessConditions;
            if ((year == TGlobals.currentActor.RunningYear) && (year != Year.第7年))
            {
                currBusinessConditions = TGlobals.currentActor.CurrBusinessConditions;
                this.tabControl.TabPages.Clear();
                this.tabControl.TabPages.Add(this.tabPage1);
            }
            else
            {
                currBusinessConditions = TGlobals.currentActor.GetBusinessConditions(year);
                this.tabControl.TabPages.Clear();
                this.tabControl.TabPages.Add(this.tabPage1);
                this.tabControl.TabPages.Add(this.tabPage2);
            }
            if (currBusinessConditions != null)
            {
                TOrderSheet promisedOrderSheet = currBusinessConditions.PromisedOrderSheet;
