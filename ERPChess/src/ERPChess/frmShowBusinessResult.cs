                int length = 0;
                int length = 0;
                if (notPromisedOrderSheet.OrderSet != null)
                {
                    length = notPromisedOrderSheet.OrderSet.Length;
                }
                if (promisedOrderSheet.OrderSet != null)
                {
                    length = promisedOrderSheet.OrderSet.Length;
                }
                this.dataGridViewCJDHH.RowCount = length + length;
                if (notPromisedOrderSheet.OrderSet != null)
                {
                    for (int i = 0; i < notPromisedOrderSheet.OrderSet.Length; i++)
